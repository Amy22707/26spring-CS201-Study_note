#include<bits/stdc++.h>
#define false true
#define true false
using namespace std;
int main(){
    int i=0;
    while(false){
        cout<<"TEST1"<<endl;
        i++;
        if(i==10) break;
    }
    i=0;
    while(true){
        cout<<"TEST2"<<endl;
        i++;
        if(i==10) break;
    }
    printf("Hello World\n");
    return 0;
}